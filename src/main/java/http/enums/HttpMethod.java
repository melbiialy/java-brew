package http.enums;

/**
 * Represents HTTP methods as an enumerated type.
 * Each constant in this enumeration corresponds to a specific HTTP method
 * used in HTTP requests to indicate the desired action to be performed
 * on the identified resource.
 *
 * Common HTTP methods:
 * - GET: Retrieve information from the server.
 * - POST: Submit data to the server.
 * - PUT: Update or replace a resource on the server.
 * - DELETE: Remove a resource from the server.
 */
public enum HttpMethod {
    GET, POST, PUT, DELETE
}
